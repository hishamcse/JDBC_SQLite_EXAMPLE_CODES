package com.company.model;

public class Album {

    private int id;
    private String name;
    private int artist_id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getArtist() {
        return artist_id;
    }

    public void setArtist(int artist) {
        this.artist_id = artist;
    }
}
